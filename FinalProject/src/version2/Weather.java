package version2;





public class Weather {
	
	private String name;
	private double temp;
	
	
	
	
	
	
	
	
	public Weather(String name, double temp) {
		super();
		this.name = name;
		this.temp = temp;
	}
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getTemp() {
		return temp;
	}
	public void setTemp(double temp) {
		this.temp = temp;
	}
	

	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}







